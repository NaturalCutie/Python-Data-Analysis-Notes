# 13_Python执行模式包括命令行模式和交互模式
# 一、命令行模式
# 写好命令后，保存并运行整个文件。运行时，Python解释器一行一行对文件进行解析和执行。

# 二、交互模式
# 输入一行后，Python立刻执行，并展示运行结果。

# 1.交互模式的优点
# 1)不需要创建新的文件，只需要进入交互式环境。
# a.进入交互式环境：Pycharm下方的Python控制台，或是在windows的CMD输入python3
# b.退出交互式环境，键入quit()，或是按ctrl + d
# 2）不需要print语句，就能直接看到执行输出的结果。如直接输入变量或算式，返回结果。

# 2.交互模式的缺点
# 1）交互模式的命令都不会被保存
